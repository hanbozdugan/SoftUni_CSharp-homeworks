using System;
class FloatDouble
{
    static void Main()
    {
        double doubleNum = 34.567839023;
        float floatNum = 12.345F;
        double secondDoubleNum = 8923.1234857;
        float secondFloatNum = 3456.091f;
        Console.WriteLine("Числата възложени за float са: {0} и {1}", floatNum, secondFloatNum);
        Console.WriteLine("Числата възложени за double са: {0} и {1}", doubleNum, secondDoubleNum);
    }
}
